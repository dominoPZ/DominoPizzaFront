package pizza.service;

public class DelAddrDTO {
	private String id;
	private String st_no;
	private String de_addr;
	private String de_kind;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSt_no() {
		return st_no;
	}
	public void setSt_no(String st_no) {
		this.st_no = st_no;
	}
	public String getDe_addr() {
		return de_addr;
	}
	public void setDe_addr(String de_addr) {
		this.de_addr = de_addr;
	}
	public String getDe_kind() {
		return de_kind;
	}
	public void setDe_kind(String de_kind) {
		this.de_kind = de_kind;
	}
	
	
	
	
}
